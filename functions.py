def print_colors(color):
    print(color)


def generate_numbers(first, second):
    numbers = range(first, second)
    for n in numbers:
        print(n)


print_colors("red")
print_colors("yellow")
print_colors("blue")

generate_numbers(0, 5)
generate_numbers(5, 10)
generate_numbers(10, 15)

